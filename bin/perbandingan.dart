void main() {
  /// == sama dengan
  /// != tidak sama dengan
  /// > lebih dari
  /// < kurang dari
  /// >= lebih dari atau sama dengan
  /// <= kurang dari atau sama dengan

  var first = 12;
  var kedua = 20;

  print(first == kedua);
  print(first != kedua);
  print(first > kedua);
  print(first < kedua);
  print(first >= kedua);
  print(first <= kedua);
}