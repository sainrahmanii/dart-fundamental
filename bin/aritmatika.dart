void main() {
  /// + untuk pertambahan
  /// - untuk pengurangan
  /// * untuk perkalian
  /// / untuk pembagian (Hasil Double)
  /// ~/ untuk pembagian (Hasil Int)
  /// % untuk hasil sisa bagi (modulus)

  var first = 23;
  var second = 12;

  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}